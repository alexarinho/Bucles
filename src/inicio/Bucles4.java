package inicio;

public class Bucles4 {

	public static void main(String[] args) {
		System.out.println("Suma de los 100 primeros numeros");
		int suma=0;
		int contador=0;
		
		for(int i=1;i<=100;i++){
			contador++;
			suma = contador + suma;
	}
		System.out.println(suma);

	}
	}
